def oxford_comma(kiwi)
"kiwi"
end
def oxford_comma("kiwi", "durian")
  "kiwi and durian"
end
def oxford_comma("kiwi", "durian", "starfruit")
  "kiwi, durian, and starfruit"
end
def oxford_comma("kiwi", "durian", "starfruit", "mangos", "dragon fruits")
  "kiwi, durian, starfruit, mangos, and dragon fruits"
